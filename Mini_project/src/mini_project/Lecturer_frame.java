    private void marks_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_marks_submitActionPerformed
         try{
            String studentregno = marks_regno.getText();
            String coursecode = marks_coursecode.getText();
            String examtype  = marks_coursetype.getSelectedItem().toString();
            Statement stmt = con.createStatement();
             switch (examtype) {
                 case "Quiz marks" -> {
                     String mystatement = "SELECT total_quiz_marks FROM tot_quiz_marks WHERE reg_no = '"+ studentregno +"' AND course_code = '"+ coursecode +"'";
                     ResultSet rs = stmt.executeQuery(mystatement);
                     while(rs.next()){
                         String score = rs.getString("total_quiz_marks");
                         result.setText(score);
                     }
                 }
                 case "Assessment" -> {
                     String mystatement2 = "SELECT total_assessment_marks FROM tot_assessment_marks WHERE reg_no = '" + studentregno +"' AND course_code = '"+ coursecode +"'";
                     ResultSet rs2 = stmt.executeQuery(mystatement2);
                     while(rs2.next()){
                         String score = rs2.getString("total_assessment_marks");
                         result.setText(score);
                     }
                 }
                 case "Mid marks" -> {
                     String mystatement3 = "SELECT total_mid_marks FROM tot_mid_marks WHERE reg_no = '"+ studentregno +"' AND course_code = '"+ coursecode +"'";
                     ResultSet rs3 = stmt.executeQuery(mystatement3);
                     while(rs3.next()){
                         String score = rs3.getString("total_mid_marks");
                         result.setText(score);
                     }
                 }
                 case "Final theory marks" -> {
                     String mystatement4 = "SELECT theory FROM final_marks WHERE reg_no = '"+ studentregno +"' AND course_code = '"+ coursecode +"'";
                     ResultSet rs4 = stmt.executeQuery(mystatement4);
                     while(rs4.next()){
                         String score = rs4.getString("theory");
                         result.setText(score);
                     }
                 }
                 case "Final practical marks" -> {
                     String mystatement5 = "SELECT practical FROM final_marks WHERE reg_no = '"+ studentregno +"' AND course_code = '"+ coursecode +"'";
                     ResultSet rs5 = stmt.executeQuery(mystatement5);
                     while(rs5.next()){
                         String score = rs5.getString("practical");
                         result.setText(score);
                     }
                 }
                 case "Final marks" -> {
                     String mystatement6 = "SELECT final_result FROM final_exam_results WHERE reg_no = '"+ studentregno +"' AND course_code = '"+ coursecode +"'";
                     ResultSet rs6 = stmt.executeQuery(mystatement6);
                     while(rs6.next()){
                         String score = rs6.getString("final_result");
                         result.setText(score);
                     }
                 }
                 default -> {
                 }
             }
            
            
            
       }catch(SQLException e){
            System.out.println("Error in loading data " + e.getMessage());
        }catch(Exception e){
            System.out.println("Error " + e.getMessage());
        }finally{
            
        }
        
    }//GEN-LAST:event_marks_submitActionPerformed

