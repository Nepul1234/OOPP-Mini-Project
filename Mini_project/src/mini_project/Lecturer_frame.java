    private void uploadImageToDatabase(File file) {
        try {
            byte[] imageData = new byte[(int) file.length()];
            try (FileInputStream fis = new FileInputStream(file)) {
                fis.read(imageData);
            }

            PreparedStatement statement = con.prepareStatement("Update user SET profile_picture  = ? WHERE fname = ?");
            statement.setBytes(1, imageData);
            statement.setString(2, myname);
            statement.executeUpdate();
            JOptionPane.showMessageDialog(this, "Image uploaded successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException | IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error uploading image: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    private void uploadFile(File file,String coursecode,String material_name) {
        try {
                    FileInputStream fis = new FileInputStream(file);
                    PreparedStatement statement = con.prepareStatement("INSERT INTO course_materials (course_code, material_name,material) VALUES (?, ?, ?)");
                    statement.setString(1, coursecode);
                    statement.setString(2,material_name);
                    statement.setBinaryStream(3, fis, (int) file.length());
                    statement.executeUpdate();

                    JOptionPane.showMessageDialog(this, "PDF file uploaded successfully.");

                    fis.close();
                    statement.close();
                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error uploading PDF file to database: " + ex.getMessage());
                }
    }
